
/**
 * Generic enums header
 * Provides empty enums to be able to compile
 */


#ifndef GENERIC_ENUMS_HEADER
#define GENERIC_ENUMS_HEADER




/** 
 * Hexadecimal codes of emulated instructions, read from the game to run
 */
enum OpCode { __GenericOpCode_Padding };


/**
 * Addressing modes for instructions
 */
enum AddressingMode { __GenericAddressingMode_Padding };


/**
 * Flags of the emulated machine's processor
 */
enum Flag { __GenericFlag_Padding };


/**
 * Processor's registers names
 */
enum RegisterName { __GenericRegisterName_Padding };




#endif /* GENERIC_ENUMS_HEADER */
